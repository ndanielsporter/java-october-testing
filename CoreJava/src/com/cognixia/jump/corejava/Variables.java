package com.cognixia.jump.corejava;

public class Variables {

	public static void main(String[] args) {
		System.out.println("I am printing to terminal");
		System.out.println(" print");
		// This is a comment
		/*
		 * multi line comment blank end of comment
		 */

		// Variables
		// Numeric
		// declare and initialize
		int i = 1;
		// declare
		int x;
		// initialize
		x = 7;
		// reassigning a variable to a new value
		i = 2;
		short s = -1;
		long l = 1000000000;
		byte b = 10;
		double d = 5.5d;
		float f = 5.5f;
		// Text
		char c = 100;
		char z = 'A';
		// Logical
		boolean bool = true;
		// String is NOT a primitive!
		String str = "a sentence";
		System.out.println(str);
		System.out.println(c);
		System.out.println(z);
		System.out.println(s + i);
	}

}
